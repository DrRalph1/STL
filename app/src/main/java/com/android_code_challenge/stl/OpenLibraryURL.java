package com.android_code_challenge.stl;

// This Class contains the Open library OpenLibraryBooks URL
public class OpenLibraryURL {

    // Static Open Library OpenLibraryBooks URL that accepts a list of International Standard Book Number's (ISBN)
    // and returns their corresponding details (such as; book cover, title, author, description etc.)
    // in a json format.
    public final static String olbURL = "https://openlibrary.org/api/books?jscmd=details&format=json&bibkeys=";

}

